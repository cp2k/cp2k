!> Get one-center two-electron Fock exchange integral for species with a given atomic number
elemental function get_onecxints_number(jang, iang, number) result(lbdll)

   !> Angular momentum of shells
   integer, intent(in) :: jang, iang

   !> Atomic number
   integer, intent(in) :: number

   !> One-center two-electron Fock exchange integral
   real(wp) :: lbdll

   if (number > 0 .and. number <= size(onecxints, dim=2)) then
      lbdll = onecxints(lidx(jang, iang), number)
   else
      lbdll = -1.0_wp
   end if

end function get_onecxints_number

