subroutine new_gfn2_calculator(calc, mol, error, accuracy)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(out) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol
   !> Error handling
   type(error_type), allocatable, intent(out) :: error
   !> Optional accuracy specification
   real(wp), intent(in), optional :: accuracy

   ! Check if all atoms of mol%nat are supported (Z <= 86)
   if (any(mol%num > max_elem)) then
      call fatal_error(error, "No support for elements with Z >" // format_string(max_elem, '(i0)') // ".")
      return
   end if

   call add_basis(calc, mol, error, accuracy)
   if (allocated(error)) return
   call add_ncoord(calc, mol, error)
   if (allocated(error)) return
   call add_hamiltonian(calc, mol)
   call add_repulsion(calc, mol)
   call add_dispersion(calc, mol, error)
   if (allocated(error)) return
   call add_coulomb(calc, mol, error)
   if (allocated(error)) return

   call add_iterator(calc, mol, accuracy)

   calc%default_etemp = default_etemp
   calc%default_guess = "sad"

   calc%method = "gfn2"

end subroutine new_gfn2_calculator

subroutine add_basis(calc, mol, error, accuracy)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(inout) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol
   !> Error handling
   type(error_type), allocatable, intent(out) :: error
   !> Optional accuracy specification
   real(wp), intent(in), optional :: accuracy

   integer :: isp, izp, ish, stat, ng
   integer, allocatable :: nsh_id(:)
   type(cgto_container), allocatable :: cgto(:, :)
   class(basis_type), allocatable :: tmp

   nsh_id = nshell(mol%num)
   allocate(cgto(maxval(nsh_id), mol%nid))
   do isp = 1, mol%nid
      izp = mol%num(isp)
      do ish = 1, nsh_id(isp)
         ng = number_of_primitives(ish, izp)
         allocate(cgto_type :: cgto(ish, isp)%raw)
         call slater_to_gauss(ng, principal_quantum_number(ish, izp), ang_shell(ish, izp), &
            & slater_exponent(ish, izp), cgto(ish, isp)%raw, .true., stat)
      end do
   end do

   allocate(tmp)
   call new_basis(tmp, mol, nsh_id, cgto, accuracy=accuracy)
   call move_alloc(tmp, calc%bas)

end subroutine add_basis

subroutine add_ncoord(calc, mol, error)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(inout) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol
   !> Error handling
   type(error_type), allocatable, intent(out) :: error

   call new_ncoord(calc%ncoord, mol, cn_count_type=cn_count%dexp, error=error)
end subroutine add_ncoord

subroutine add_hamiltonian(calc, mol)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(inout) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol

   call new_hamiltonian(calc%h0, mol, calc%bas, new_gfn2_h0spec(mol))
end subroutine add_hamiltonian

