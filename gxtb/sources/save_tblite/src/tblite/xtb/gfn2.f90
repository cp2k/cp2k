   type(d4_dispersion), allocatable :: tmp

   allocate(tmp)
   call new_d4_dispersion(tmp, mol, twobody_damping_function%rational, &
      & threebody_damping_function%zero_avg, s6=s6, s8=s8, a1=a1, a2=a2, s9=s9, &
      & error=error)
   call move_alloc(tmp, calc%dispersion)
end subroutine add_dispersion

subroutine add_repulsion(calc, mol)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(inout) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol

   type(gfn_repulsion), allocatable :: tmp

   real(wp), allocatable :: alpha(:), zeff(:)

   alpha = rep_alpha(mol%num)
   zeff = rep_zeff(mol%num)
   allocate(tmp)
   call new_repulsion_gfn(tmp, mol, alpha=alpha, zeff=zeff, &
      & kexp=rep_kexp, kexp_light=rep_kexp_light, rexp=rep_rexp)
   call move_alloc(tmp, calc%repulsion)
end subroutine add_repulsion

subroutine add_coulomb(calc, mol, error)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(inout) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol
   !> Error handling
   type(error_type), allocatable, intent(out) :: error

   real(wp), allocatable :: hardness(:, :), hubbard_derivs(:, :)
   real(wp), allocatable :: dkernel(:), qkernel(:), rad(:), vcn(:)
   type(effective_coulomb), allocatable :: es2
   type(onsite_thirdorder), allocatable :: es3
   type(gfn2_multipole), allocatable :: aes2
   type(average_type), allocatable :: average

   allocate(calc%coulomb)
   call new_coulomb(calc%coulomb, mol, error, cn_count%dexp)
   if (allocated(error)) return

   allocate(es2)
   call get_shell_hardness(mol, calc%bas, hardness)
   allocate(average)
   call new_average(average, average_id%arithmetic)
   call new_effective_coulomb(es2, mol, gexp, hardness, average, &
      & nshell=calc%bas%nsh_id)
   call move_alloc(es2, calc%coulomb%es2)

   allocate(es3)
   call get_hubbard_derivs(mol, calc%bas, hubbard_derivs)
   call new_onsite_thirdorder(es3, mol, hubbard_derivs, calc%bas%nsh_id)
   call move_alloc(es3, calc%coulomb%es3)

   allocate(aes2)
   dkernel = p_dkernel(mol%num)
   qkernel = p_qkernel(mol%num)
   rad = p_rad(mol%num)
   vcn = p_vcn(mol%num)
   call new_average(average, average_id%arithmetic)
   call new_gfn2_multipole(aes2, mol, dkernel, qkernel, mp_shift, mp_kradexp, &
      & mp_rmax, rad, average, vcn, kdmp3=1.0_wp, kdmp5=1.0_wp, &
      & kexp3=mp_exp3, kexp5=mp_exp5)
   call move_alloc(aes2, calc%coulomb%aes2)

end subroutine add_coulomb

subroutine get_shell_hardness(mol, bas, hardness)
   !> Molecular structure data
   type(structure_type), intent(in) :: mol
   !> Basis set information
   class(basis_type), intent(in) :: bas
   !> Shell resolved hardness parameters
   real(wp), allocatable, intent(out) :: hardness(:, :)

   integer :: isp, izp, ish, il

   allocate(hardness(maxval(bas%nsh_id), mol%nid))
   hardness(:, :) = 0.0_wp
   do isp = 1, mol%nid
      izp = mol%num(isp)
      do ish = 1, bas%nsh_id(isp)
         il = bas%cgto(ish, isp)%raw%ang
         hardness(ish, isp) = hubbard_parameter(izp) * shell_hubbard(il, izp)
      end do
   end do
end subroutine get_shell_hardness

subroutine add_iterator(calc, mol, accuracy)
   !> Instance of the xTB evaluator
   type(xtb_calculator), intent(inout) :: calc
   !> Molecular structure data
   type(structure_type), intent(in) :: mol
   !> Optional accuracy specification
   real(wp), intent(in), optional :: accuracy

   type(mixer_input_container), allocatable :: mixer_inputs(:)

   allocate(mixer_inputs(2))
   allocate(mixer_inputs(1)%raw, source=simple_input(mode=mixer_mode%density))
