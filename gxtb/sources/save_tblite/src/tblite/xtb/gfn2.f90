elemental function kshell(k, l)
   integer, intent(in) :: k, l
   real(wp) :: kshell
   kshell = merge(2.0_wp, (kdiag(l)+kdiag(k))/2, k==2.and.any(l==[0,1]).or.l==2.and.any(k==[0,1]))
end function kshell

